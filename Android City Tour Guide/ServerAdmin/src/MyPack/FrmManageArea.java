    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        DlgAddArea d = new DlgAddArea(this);
        d.setVisible(true);
        if(d.areaInfo == null) return;
        try{
            Statement st = parent.con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_UPDATABLE);
            st.executeUpdate("insert into area values('" + d.areaInfo.areaName + "','" + d.areaInfo.latitude + "','" + d.areaInfo.longitude + "')");
            fillList(jTextField1.getText());
        }catch(Exception e){
            System.out.println("Error adding area : " + e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        if(index == -1) return;
        try{
            Statement st = parent.con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_UPDATABLE);
            st.executeUpdate("delete from area where areaname = '" + tm.getValueAt(index, 0) + "'");
            fillList(jTextField1.getText());
        }catch(Exception e){
            System.out.println("Error deleting area : " + e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

