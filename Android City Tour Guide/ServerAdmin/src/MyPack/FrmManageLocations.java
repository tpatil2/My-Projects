    void fillPanels(){
        String catLoc = "";
        String ser = jTextField1.getText();
        if(jComboBox1.getSelectedIndex() > 0){
            catLoc = jComboBox1.getSelectedItem().toString();
        }
        jScrollPane1.getViewport().removeAll();
        jPanelHolder.removeAll();
        try{
            Statement st = parent.con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_UPDATABLE);
            String ssql = "select * from location where locationname like '%" + ser + "%' and category like '%" + catLoc + "%'";
            ResultSet rs = st.executeQuery(ssql);
            rs.last();
            int maxRow = rs.getRow();
            if(maxRow < 4) maxRow = 4;
            jPanelHolder.setLayout(new GridLayout(maxRow, 1));
            rs.beforeFirst();
            while(rs.next()){
                SingleLocation sl = new SingleLocation();
                sl.category = rs.getString("category");
                sl.area.areaName = rs.getString("area");
                sl.description = rs.getString("description");
                sl.name = rs.getString("locationname");
                sl.image = getIntArray(rs.getBytes("image"));
                System.out.println("Leng " + sl.image.length);
                sl.tid = rs.getInt("tid");
                PlnLocation p = new PlnLocation(sl);
                p.addMouseListener(this);
                pnlVector.addElement(p);
                jPanelHolder.add(p);
            }
            jScrollPane1.getViewport().add(jPanelHolder);
        }catch(Exception e){
            System.out.println("Error Filling data : " + e);
            e.printStackTrace();
        }
    }

    @Override
    public void mouseClicked(MouseEvent e) {
        for(int i=0;i<pnlVector.size();i++){
            if(e.getSource().equals(pnlVector.elementAt(i))){
                if(selIndex != -1){
                    pnlVector.elementAt(selIndex).setSelected(false);
                }
                selIndex = i;
                pnlVector.elementAt(selIndex).setSelected(true);
                break;
            }
        }
    }

