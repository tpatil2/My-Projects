    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        DlgAddNewLocation d = new DlgAddNewLocation(parent, true);
        d.setVisible(true);
        if(d.location == null) return;
        
        try{
            Statement st = parent.con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_UPDATABLE);
            String ssql = "insert into location values(?,?,?,?,?,?)";
            PreparedStatement pre;
            ByteArrayInputStream bis = new ByteArrayInputStream(getByteArray(d.location.image));
            pre = parent.con.prepareStatement(ssql);
            pre.setInt(1, parent.getNextID("location", "tid"));
            pre.setString(2, d.location.category);
            pre.setString(3, d.location.area.areaName);
            pre.setString(4, d.location.name);
            pre.setString(6, d.location.description);
            pre.setBinaryStream(5, bis, (100 * 80 * 4));
            pre.executeUpdate();
            fillPanels();
        }catch(Exception e){
            System.out.println("Error adding advt : " + e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    byte[] getByteArray(int[] arr){
        byte[] ret = new byte[arr.length * 4];
        
        for(int i=0,j=0;i<arr.length;i++,j+=4){
            ret[j+0] = (byte)(arr[i] & 0xff);
            ret[j+1] = (byte)((arr[i] >> 8) & 0xff);
            ret[j+2] = (byte)((arr[i] >> 16) & 0xff);
            ret[j+3] = (byte)((arr[i] >> 24) & 0xff);
        }
        
        return ret;
    }
    
    int[] getIntArray(byte[] arr){
        int[] ret = new int[arr.length / 4];
        
        for(int i=0,j=0;i<ret.length;i++,j+=4){
            ret[i] = arr[j] | (arr[j+1] << 8) | (arr[j+2] << 16) | (arr[j+3] << 24);
        }
        
        return ret;
    }
