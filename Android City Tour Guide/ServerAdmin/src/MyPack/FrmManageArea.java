    void fillList(String str){
        tm = new DefaultTableModel(colHeader, 0);
        try{
            Statement st = parent.con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("select * from area where areaname like '%" + str + "%'");
            String area = "";
            while(rs.next()){
                String[] sarr = new String[] {rs.getString(1), rs.getString(2), rs.getString(3)};
                tm.addRow(sarr);
            }
        }catch(Exception e){
            System.out.println("Error Filling Area List : " + e);
        }
        jTable1.setModel(tm);
    }
    
